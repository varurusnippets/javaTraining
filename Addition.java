class Addition{
	
	
	public int add(int i, int j){
		
		return i+j;
	}
	
	public float add(float i, float j){
		
		return i+j;
	}
	
	
	public int add(int i, int j, int k){
		
		return i+j+k;
	}
	
	
	public static void main(String [] args){
		
		
		System.out.println( new Addition().add(3.0f,4.0f));
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}